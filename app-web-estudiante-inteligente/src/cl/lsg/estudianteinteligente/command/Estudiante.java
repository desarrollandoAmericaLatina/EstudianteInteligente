package cl.lsg.estudianteinteligente.command;

public class Estudiante {
	private String nombre;
	private String rut;
	
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getNombre() {
		return nombre;
	}
	public void setRut(String rut) {
		this.rut = rut;
	}
	public String getRut() {
		return rut;
	}
}
